          <v-list-item-icon class="ma-0 mr-2 mt-2">
            <v-icon>mdi-circle-edit-outline</v-icon>
          </v-list-item-icon>
          <v-list-item-content>
            <v-list-item-title>Установить пользовательское описание</v-list-item-title>
          </v-list-item-content>
        </v-list-item>
