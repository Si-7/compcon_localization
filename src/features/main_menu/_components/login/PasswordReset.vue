<template>
  <div>
    <v-row dense class="panel" justify="center" align="center">
      <v-col cols="auto" style="letter-spacing: 5px">СБРОСИТЬ ПАРОЛЬ</v-col>
    </v-row>
    <v-row class="mt-2" justify="center">
      <v-col lg="6" cols="12">
        <v-text-field v-model="email" outlined dense label="Адрес электронной почты" hide-details />
      </v-col>
    </v-row>
    <v-row no-gutters justify="center" align="start" class="mt-n2 text-center">
      <v-col cols="auto">
        <v-btn color="secondary" :disabled="!email" :loading="loading" @click="reset()">
          Отправить письмо сброса пароля
        </v-btn>
      </v-col>
    </v-row>
    <v-slide-x-transition group>
      <v-row v-if="sent" key="a">
        <v-col cols="12" class="text-center body-text stark--text">
          <div>Код сброса пароля отправлен на {{ email }}</div>
        </v-col>
        <v-col lg="6" cols="12">
          <v-text-field v-model="code" outlined dense label="Код сброса пароля" />
        </v-col>
        <v-col lg="6" cols="12">
          <v-text-field
            v-model="newpass"
            outlined
            dense
            label="Новый пароль"
            :append-icon="show ? 'mdi-eye' : 'mdi-eye-off'"
            :type="show ? 'text' : 'password'"
            :rules="[rules.passLength]"
            @click:append="show = !show"
          />
        </v-col>
      </v-row>
      <v-row v-if="sent" key="b" no-gutters justify="end" class="mt-n2">
        <v-col cols="auto">
          <v-btn
            text
            color="primary"
            :loading="loading"
            :disabled="!newpass"
            @click="setNewPassword()"
          >
            Отправить
          </v-btn>
        </v-col>
      </v-row>
    </v-slide-x-transition>
    <v-row justify="center">
      <v-col cols="auto">
        <v-btn text color="accent" class="mt-1" @click="$emit('set-state', 'sign-in')">
          Отменить
        </v-btn>
      </v-col>
    </v-row>
  </div>
</template>

<script lang="ts">
import Vue from 'vue'
import { Auth } from '@aws-amplify/auth'

export default Vue.extend({
  name: 'auth-password-reset',
  data: () => ({
    loading: false,
    sent: false,
    email: null,
    show: false,
    oldpass: null,
    newpass: null,
    resetpass: null,
    code: null,
    rules: {
      passLength: v => (v && v.length >= 6) || 'Minimum 6 characters',
    },
  }),
  methods: {
    reset() {
      this.loading = true
      Auth.forgotPassword(this.email)
        .then(data => {
          console.log(data)
          this.loading = false
          this.sent = true
        })
        .catch(err => {
          console.log('err at first catch:', err)
          Auth.forgotPassword(this.email.toLowerCase())
            .then(data => {
              console.log(data)
              this.loading = false
              this.sent = true
            })
            .catch(err => {
              console.error(err)
              this.loading = false
              this.sent = false
              this.$notify(`Unable to send reset e-mail: ${err.message}`, 'error')
            })
        })
    },
    setNewPassword() {
      this.loading = true
      Auth.forgotPasswordSubmit(this.email, this.code, this.newpass)
        .then(data => {
          console.log(data)
          this.loading = false
          this.$notify('Password changed successfully. Please log in again')
          this.$emit('set-state', 'sign-in')
        })
        .catch(err => {
          console.log(err)
          this.$notify(`Unable to change password: ${err.message}`, 'error')
          this.loading = false
        })
    },
  },
})
</script>
